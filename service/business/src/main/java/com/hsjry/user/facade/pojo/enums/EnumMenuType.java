package com.hsjry.user.facade.pojo.enums;

/**
 * 菜单类型
 * @author hongsj
 * @version $Id: EnumMenuTypeCode.java, v 1.0 2017年3月7日 下午2:02:53 hongsj Exp $
 */
public enum EnumMenuType {
    
    JOB("0","工单"),
    URL("1","菜单URL"),
    SERVICE_NO("2","交易服务号"),
    FUNCTION_URL("3","功能URL");
    
    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumMenuType(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumMenuType find(String code) {
        for (EnumMenuType frs : EnumMenuType.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }

}
