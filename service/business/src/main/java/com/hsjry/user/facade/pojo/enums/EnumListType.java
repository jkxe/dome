package com.hsjry.user.facade.pojo.enums;

/**
 * 名单类型
 * @author hongsj
 * @version $Id: EnumListType.java, v 1.0 2017年3月23日 下午3:48:53 hongsj Exp $
 */
public enum EnumListType {
    UNCLASSIFIED("0", "未分类"),
    CREDIT("1","信用");

    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumListType(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumListType find(String code) {
        for (EnumListType frs : EnumListType.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }
}
