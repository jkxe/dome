package com.hsjry.user.facade.pojo.enums;

/**
 * 教育类型
 * 
 * @author wanglg15468
 * @version $Id: EnumEducationType.java, v 1.0 2017-3-14 下午8:03:43 wanglg15468 Exp $
 */
public enum EnumEducationType {
    
    FULL_TIME("1", "全日制"),
    PART_TIME("2", "在职");
    
    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumEducationType(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumEducationType find(String code) {
        for (EnumEducationType frs : EnumEducationType.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }
}
