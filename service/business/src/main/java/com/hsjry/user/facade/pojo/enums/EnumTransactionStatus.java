package com.hsjry.user.facade.pojo.enums;

/**
 * 代扣签约交易状态
 * 
 * @author lilin
 * @version $Id: EnumTransactionStatus.java, v 1.0 2017年12月14日 上午11:21:59 lilin Exp $
 */
public enum EnumTransactionStatus {
    
    IN_PROCESS("2", "处理中"),
    SUCCESS("3", "成功"),
    FAIL("7", "失败");

    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumTransactionStatus(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumTransactionStatus find(String code) {
        for (EnumTransactionStatus frs : EnumTransactionStatus.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }
}
