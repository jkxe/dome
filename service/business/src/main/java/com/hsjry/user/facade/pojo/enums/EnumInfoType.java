package com.hsjry.user.facade.pojo.enums;

/**
 * 征信信息类别
 * 
 * @author wanglg15468
 * @version $Id: EnumInfoType.java, v 1.0 2017-3-14 下午7:56:22 wanglg15468 Exp $
 */
public enum EnumInfoType {
                               
    POSITIVE_INFO("1", "正面信息"),
    NEGATIVE_INFO("2", "负面信息"),
    NEUTRAL_INFO("3", "中立信息");

    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumInfoType(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumInfoType find(String code) {
        for (EnumInfoType frs : EnumInfoType.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }
}
