package com.hsjry.user.facade.pojo.enums;

/**
 * object状态
 * @author hongsj
 * @version $Id: EnumObjectStatusCode.java, v 1.0 2017年3月8日 上午10:32:05 hongsj Exp $
 */
public enum EnumObjectStatus {

    DISABLE("0", "停用"),
    ENABLE("1", "启用"); 

    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumObjectStatus(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumObjectStatus find(String code) {
        for (EnumObjectStatus frs : EnumObjectStatus.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }

}
