package com.hsjry.user.facade.pojo.enums;

/**
 * 动作
 * @author hongsj
 * @version $Id: EnumActionType.java, v 1.0 2017年4月6日 下午6:26:09 hongsj Exp $
 */
public enum EnumActionType {
    UNBIND("0", "解绑"), 
    BIND("1", "绑定");

    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumActionType(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumActionType find(String code) {
        for (EnumActionType frs : EnumActionType.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }
}
