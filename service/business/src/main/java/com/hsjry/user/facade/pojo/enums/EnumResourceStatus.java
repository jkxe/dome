package com.hsjry.user.facade.pojo.enums;

/**
 * 资源状态
 * @author hongsj
 * @version $Id: EnumResourceStatusCode.java, v 1.0 2017年3月7日 下午1:54:27 hongsj Exp $
 */
public enum EnumResourceStatus {
    /**0-未验证*/
    UNVERIFIED("0","未验证"),
    /**1-生效*/
    EFFECTIVE("1", "生效"),
    /**2-失效*/
    DISABLE("2", "失效");

    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumResourceStatus(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumResourceStatus find(String code) {
        for (EnumResourceStatus frs : EnumResourceStatus.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }
}
