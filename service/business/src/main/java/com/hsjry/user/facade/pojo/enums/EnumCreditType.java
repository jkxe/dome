package com.hsjry.user.facade.pojo.enums;

/**
 * 信贷类型
 * 
 * @author wanglg15468
 * @version $Id: EnumCreditType.java, v 1.0 2017-3-14 下午7:50:53 wanglg15468 Exp $
 */
public enum EnumCreditType {
                               
    LOAN("1", "贷款"),
    EXTERNAL_GUARANTEE("2", "对外担保");
    
    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumCreditType(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumCreditType find(String code) {
        for (EnumCreditType frs : EnumCreditType.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }
}
