package com.hsjry.user.facade.pojo.enums;

/**
 * 角色类型枚举
 * @author hongsj
 * @version $Id: EnumRoleTypeCode.java, v 1.0 2017年3月7日 下午1:27:28 hongsj Exp $
 */
public enum EnumRoleType {
    
    SYSTEM_ROLE("1", "系统角色"),
    CUSTOM_ROLE("2", "自定义角色");

    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumRoleType(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumRoleType find(String code) {
        for (EnumRoleType frs : EnumRoleType.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }
}
