package com.hsjry.user.facade.pojo.enums;

/**
 * 渠道类型
 * @author hongsj
 * @version $Id: EnumChannelType.java, v 1.0 2017年3月15日 上午10:21:02 hongsj Exp $
 */
public enum EnumChannelType {
    
    INSIDE("1","内部"),
    OUTSIDE("2","外部");
    
    /** 状态码 **/
    private String code;
    /** 状态描述 **/
    private String description;

    /**
    * 私有构造方法
    * 
    * @param code
    *            编码
    * @param description
    *            描述
    **/
    private EnumChannelType(String code, String description) {
        this.code = code;
        this.description = description;
    }

    /**
    * 根据编码查找枚举
    * 
    * @param code
    *            编码
    * @return {@link EnumBearingMode} 实例
    **/
    public static EnumChannelType find(String code) {
        for (EnumChannelType frs : EnumChannelType.values()) {
            if (frs.getCode().equals(code)) {
                return frs;
            }
        }
        return null;
    }

    /**
    * Getter method for property <tt>code</tt>.
    * 
    * @return property value of code
    **/
    public String getCode() {
        return code;
    }

    /**
    * Getter method for property <tt>description</tt>.
    * 
    * @return property value of description
    **/
    public String getDescription() {
        return description;
    }
}
